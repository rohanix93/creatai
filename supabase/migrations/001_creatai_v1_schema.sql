-- Helper: same 4 policies repeated per table
do $$
declare
  t text;
begin
  for t in
    select unnest(array[
      'brands','competitors','creators','content_assets',
      'creative_dna_reports','creative_clusters','ai_chat_messages'
    ])
  loop
    execute format(
      'drop policy if exists "select_own" on public.%I; create policy "select_own" on public.%I for select using (auth.uid() = owner_id);', t, t
    );
    execute format(
      'drop policy if exists "insert_own" on public.%I; create policy "insert_own" on public.%I for insert with check (auth.uid() = owner_id);', t, t
    );
    execute format(
      'drop policy if exists "update_own" on public.%I; create policy "update_own" on public.%I for update using (auth.uid() = owner_id);', t, t
    );
    execute format(
      'drop policy if exists "delete_own" on public.%I; create policy "delete_own" on public.%I for delete using (auth.uid() = owner_id);', t, t
    );
  end loop;
end $$;

-- ============================================================
-- updated_at triggers
-- ============================================================
create or replace function public.set_updated_at() returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

drop trigger if exists brands_updated_at on public.brands;
create trigger brands_updated_at before update on public.brands
  for each row execute function public.set_updated_at();

drop trigger if exists assets_updated_at on public.content_assets;
create trigger assets_updated_at before update on public.content_assets
  for each row execute function public.set_updated_at();

drop trigger if exists clusters_updated_at on public.creative_clusters;
create trigger clusters_updated_at before update on public.creative_clusters
  for each row execute function public.set_updated_at();

-- ============================================================
-- STORAGE :: thumbnails bucket
-- ============================================================
insert into storage.buckets (id, name, public)
values ('thumbnails', 'thumbnails', true)
on conflict (id) do nothing;

-- Public read for thumbnails
drop policy if exists "public can read thumbnails" on storage.objects;
create policy "public can read thumbnails"
  on storage.objects for select
  using (bucket_id = 'thumbnails');

-- Authenticated upload to thumbnails
drop policy if exists "auth can upload thumbnails" on storage.objects;
create policy "auth can upload thumbnails"
  on storage.objects for insert
  with check (bucket_id = 'thumbnails' and auth.role() = 'authenticated');

-- Owners can delete their thumbnails (by path convention: <user_id>/...)
drop policy if exists "owner can delete thumbnails" on storage.objects;
create policy "owner can delete thumbnails"
  on storage.objects for delete
  using (bucket_id = 'thumbnails' and (auth.uid())::text = split_part(name, '/', 1));
