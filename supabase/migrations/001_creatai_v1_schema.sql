-- ============================================================
-- CONTENT ASSETS — every analyzed piece of content
-- ============================================================
create table if not exists public.content_assets (
  id uuid primary key default gen_random_uuid(),
  owner_id uuid not null references auth.users(id) on delete cascade,
  brand_id uuid references public.brands(id) on delete set null,

  -- source
  source_url text,
  platform text,                  -- instagram | tiktok | youtube | linkedin | meta_ad | other
  content_type text,              -- video | image | carousel | story | ad

  -- linked entities (optional)
  creator_id uuid references public.creators(id) on delete set null,
  competitor_id uuid references public.competitors(id) on delete set null,

  -- display metadata
  title text,
  campaign_name text,
  creator_name text,              -- denormalized for fast display
  competitor_name text,

  -- raw inputs
  caption text,
  transcript text,
  thumbnail_url text,             -- storage path or external URL

  -- manual metrics
  views integer,
  likes integer,
  comments integer,
  shares integer,
  saves integer,
  spend numeric,
  conversions integer,
  notes text,

  -- extraction metadata
  extraction_status text default 'pending',     -- pending | extracted | failed | manual
  extraction_source text,                        -- youtube | vision | whisper | manual
  extracted_at timestamptz,

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

